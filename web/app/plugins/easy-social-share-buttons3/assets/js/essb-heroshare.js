var heroshareTriggeredOnScroll = false;
var heroshareTriggerPercent = -1;
var heroshareTriggerEnd = false;
var heroshareTriggerExit = false;

var heroImageWidth = 0;


var essb_heroshare_exit = function() {
	if (heroshareTriggerExit) { return; }
	
	var e = window.event;
	
	var from = e.relatedTarget || e.toElement;

	// Reliable, works on mouse exiting window and user switching active program
	if(!from || from.nodeName === "HTML") {
		heroshareTriggerExit = true;
		essb_heroshare_show();
	}
}

var essb_heroshare_onscroll = function() {
	if (heroshareTriggeredOnScroll) { return; }
	
	var current_pos = jQuery(window).scrollTop();
	var height = jQuery(document).height()-jQuery(window).height();
	var percentage = current_pos/height*100;	
	
	if (!heroshareTriggerEnd) {
		if (percentage > heroshareTriggerPercent && heroshareTriggerPercent > 0) {
			heroshareTriggeredOnScroll = true;
			essb_heroshare_show();
		}
	}
	else {
		var element = jQuery('.essb_break_scroll');
		if (!element.length) { 
			var userTriggerPercent = 90;
			if (percentage > userTriggerPercent && userTriggerPercent > 0) {
				heroshareTriggeredOnScroll = true;
				essb_heroshare_show();
			}
		}
		else {
			var top = jQuery('.essb_break_scroll').offset().top - parseFloat(jQuery('.essb_break_scroll').css('marginTop').replace(/auto/, 0));
			
			if (current_pos >= top) {
				heroshareTriggeredOnScroll = true;
				essb_heroshare_show();
			}
		}
	}
}

